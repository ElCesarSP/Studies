/**
 * Tipo de dados primitivel | 
 * 
 * String
 * Number
 * UnderFined
 * Boolean (true / false)
 */

const nome = 'César';  // String
const nome1 = "César";  // String
const nome2 = `César`;  // String

const number = 10;  // Number
const number1 = 12.23;  // Number

let nomeAluno;  // UnderFined = não e criado valor nenhuma na memória

let sobreNomeAlunu = null;  // Nulo = não e criado valor nenhuma na memória

const aprovado = true;  // Boolean = true / false  (Valores lógicos)

console.log(typeof nome); // String
console.log(typeof nome1); //  String
console.log(typeof nome2); // String
console.log(typeof number); // Number
console.log(typeof number1); //Number float
console.log(typeof nomeAluno); // UnderFined 
console.log(typeof sobreNomeAlunu);  // Nulo ( UnderFined) Object
console.log(typeof aprovado);  // Boolean